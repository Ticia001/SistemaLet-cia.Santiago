    private void jBtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExcluirActionPerformed
        // TODO add your handling code here:
        int resp = JOptionPane.showConfirmDialog(null,
                "Deseja excluir o registro?", "Aviso",
                JOptionPane.YES_NO_OPTION);
        if (resp == JOptionPane.YES_OPTION) {
            UsuariosDAO usuariosDAO = new UsuariosDAO();
            usuarios usuarios = new usuarios();
            usuarios.setIdusuarios(Integer.valueOf(jTxtCodigo.getText()));
            usuariosDAO.delete(usuarios);
            //pegar as informacoes da tela e jogar no bean usuarios
            //chamar o metodo delete do dao passando o bean usuarios
            JOptionPane.showMessageDialog(null, "Exclusão efetuada.");
        }
        Util.limpar(jTxtCodigo, jTxtNome, jTxtApelido, jFmtCpf, jFmtData, jPwfSenha, jCboNivel, jChbAtivo);

    }//GEN-LAST:event_jBtnExcluirActionPerformed

