    private void jBtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfirmarActionPerformed
       usuarios = viewBean();
        //chamar o metodo insert do dao
        UsuariosDAO usuariosDAO = new UsuariosDAO();
        if (incluindo == true) {
            usuariosDAO.insert(usuarios);
        } else {
            usuariosDAO.update(usuarios);
        }
        Util.habilitar(false,jTxtNome, jTxtCodigo, jTxtApelido, jFmtCpf, jFmtData, jCboNivel, jChbAtivo, jPwfSenha);
        Util.limpar(jTxtCodigo, jTxtNome, jTxtApelido, jFmtCpf, jFmtData, jPwfSenha, jCboNivel, jChbAtivo);
    }//GEN-LAST:event_jBtnConfirmarActionPerformed

